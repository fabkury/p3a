/* Note: memcpy blit does not support chroma key transparency. */
void reaction_revoke_img_blit_memcpy_bgr888(
    uint8_t *dst,
    int dst_w,
    int dst_h,
    int dst_stride_bytes,
    int x,
    int y
)
{
    const int src_w = reaction_revoke_img_w;
    const int src_h = reaction_revoke_img_h;

    /* Compute source and destination regions with clipping */
    int src_x = 0;
    int src_y = 0;
    int copy_w = src_w;
    int copy_h = src_h;

    /* Clip left edge */
    if (x < 0) {
        src_x = -x;
        copy_w += x;
        x = 0;
    }

    /* Clip top edge */
    if (y < 0) {
        src_y = -y;
        copy_h += y;
        y = 0;
    }

    /* Clip right edge */
    if (x + copy_w > dst_w) {
        copy_w = dst_w - x;
    }

    /* Clip bottom edge */
    if (y + copy_h > dst_h) {
        copy_h = dst_h - y;
    }

    /* If fully clipped, nothing to draw */
    if (copy_w <= 0 || copy_h <= 0) {
        return;
    }

    /* Blit row by row using memcpy */
    const int src_stride = src_w * 3;
    const int copy_bytes = copy_w * 3;

    for (int j = 0; j < copy_h; j++) {
        const uint8_t *src_row = reaction_revoke_img_pixels + (src_y + j) * src_stride + (src_x * 3);
        uint8_t *dst_row = dst + (y + j) * dst_stride_bytes + (x * 3);
        memcpy(dst_row, src_row, copy_bytes);
    }
}

void reaction_revoke_img_blit_pixelwise_bgr888(
    uint8_t *dst,
    int dst_w,
    int dst_h,
    int dst_stride_bytes,
    int x,
    int y,
    uint8_t alpha,
    uint8_t bg_b,
    uint8_t bg_g,
    uint8_t bg_r,
    int scale,
    int rotation
)
{
    const int src_w = reaction_revoke_img_w;
    const int src_h = reaction_revoke_img_h;

    /* Treat scale <= 1 as no scaling */
    if (scale <= 1) scale = 1;

    /* For 90/270 rotation, output dimensions are swapped */
    int scaled_w, scaled_h;
    if (rotation == 90 || rotation == 270) {
        scaled_w = src_h * scale;
        scaled_h = src_w * scale;
    } else {
        scaled_w = src_w * scale;
        scaled_h = src_h * scale;
    }

    /* Compute clipping in scaled coordinates */
    int dst_x0 = x;
    int dst_y0 = y;
    int dst_x1 = x + scaled_w;
    int dst_y1 = y + scaled_h;

    /* Clip to destination bounds */
    if (dst_x0 < 0) dst_x0 = 0;
    if (dst_y0 < 0) dst_y0 = 0;
    if (dst_x1 > dst_w) dst_x1 = dst_w;
    if (dst_y1 > dst_h) dst_y1 = dst_h;

    /* If fully clipped, nothing to draw */
    if (dst_x0 >= dst_x1 || dst_y0 >= dst_y1) {
        return;
    }

    /* Precompute rotation coefficients so the branch runs once, not per-pixel.
     * src_x_idx = base_sx + cx_ox * ox + cx_oy * oy
     * src_y_idx = base_sy + cy_ox * ox + cy_oy * oy */
    int base_sx, cx_ox, cx_oy, base_sy, cy_ox, cy_oy;
    if (rotation == 90) {
        base_sx = 0;         cx_ox =  0; cx_oy = 1;
        base_sy = src_h - 1; cy_ox = -1; cy_oy = 0;
    } else if (rotation == 180) {
        base_sx = src_w - 1; cx_ox = -1; cx_oy =  0;
        base_sy = src_h - 1; cy_ox =  0; cy_oy = -1;
    } else if (rotation == 270) {
        base_sx = src_w - 1; cx_ox = 0; cx_oy = -1;
        base_sy = 0;         cy_ox = 1; cy_oy =  0;
    } else {
        base_sx = 0; cx_ox = 1; cx_oy = 0;
        base_sy = 0; cy_ox = 0; cy_oy = 1;
    }

    /* Optimized code paths based on alpha value */
    if (alpha == 0) {
        /* Alpha = 0: Fill region with background color only */
        for (int dy = dst_y0; dy < dst_y1; dy++) {
            uint8_t *dst_row = dst + dy * dst_stride_bytes;
            for (int dx = dst_x0; dx < dst_x1; dx++) {
                uint8_t *dst_px = dst_row + dx * 3;
                dst_px[0] = bg_b;
                dst_px[1] = bg_g;
                dst_px[2] = bg_r;
            }
        }
    } else if (alpha == 255) {
        /* Alpha = 255: Opaque blit, no blending needed */
        for (int dy = dst_y0; dy < dst_y1; dy++) {
            uint8_t *dst_row = dst + dy * dst_stride_bytes;

            for (int dx = dst_x0; dx < dst_x1; dx++) {
                int ox = (dx - x) / scale;
                int oy = (dy - y) / scale;
                int src_x_idx = base_sx + cx_ox * ox + cx_oy * oy;
                int src_y_idx = base_sy + cy_ox * ox + cy_oy * oy;
                const uint8_t *src_px = reaction_revoke_img_pixels + (src_y_idx * src_w + src_x_idx) * 3;

                if (src_px[0] == REACTION_REVOKE_IMG_CHROMA_KEY_B &&
                    src_px[1] == REACTION_REVOKE_IMG_CHROMA_KEY_G &&
                    src_px[2] == REACTION_REVOKE_IMG_CHROMA_KEY_R) continue;

                uint8_t *dst_px = dst_row + dx * 3;
                dst_px[0] = src_px[0];  /* B */
                dst_px[1] = src_px[1];  /* G */
                dst_px[2] = src_px[2];  /* R */
            }
        }
    } else {
        /* 0 < alpha < 255: Alpha blend with background */
        const int inv_alpha = 255 - alpha;

        for (int dy = dst_y0; dy < dst_y1; dy++) {
            uint8_t *dst_row = dst + dy * dst_stride_bytes;

            for (int dx = dst_x0; dx < dst_x1; dx++) {
                int ox = (dx - x) / scale;
                int oy = (dy - y) / scale;
                int src_x_idx = base_sx + cx_ox * ox + cx_oy * oy;
                int src_y_idx = base_sy + cy_ox * ox + cy_oy * oy;
                const uint8_t *src_px = reaction_revoke_img_pixels + (src_y_idx * src_w + src_x_idx) * 3;

                if (src_px[0] == REACTION_REVOKE_IMG_CHROMA_KEY_B &&
                    src_px[1] == REACTION_REVOKE_IMG_CHROMA_KEY_G &&
                    src_px[2] == REACTION_REVOKE_IMG_CHROMA_KEY_R) {
                    uint8_t *dst_px = dst_row + dx * 3;
                    dst_px[0] = bg_b;
                    dst_px[1] = bg_g;
                    dst_px[2] = bg_r;
                    continue;
                }

                uint8_t *dst_px = dst_row + dx * 3;
                /* Composite: out = src * alpha + bg * (1 - alpha) */
                dst_px[0] = (uint8_t)((src_px[0] * alpha + bg_b * inv_alpha + 127) / 255);
                dst_px[1] = (uint8_t)((src_px[1] * alpha + bg_g * inv_alpha + 127) / 255);
                dst_px[2] = (uint8_t)((src_px[2] * alpha + bg_r * inv_alpha + 127) / 255);
            }
        }
    }
}
